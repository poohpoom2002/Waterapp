export const LanguageHorticultureProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
    const [language, setLanguage] = useState<Language>('th');

    useEffect(() => {
        // Load language preference from localStorage
        const savedLanguage = localStorage.getItem('language') as Language;
        if (savedLanguage && (savedLanguage === 'en' || savedLanguage === 'th')) {
            setLanguage(savedLanguage);
        } else {
            // Set default to Thai if no saved preference
            setLanguage('th');
            localStorage.setItem('language', 'th');
        }
    }, []);

    const handleSetLanguage = (lang: Language) => {
        setLanguage(lang);
        localStorage.setItem('language', lang);
    };

    const t = (key: string): string => {
        return horticultureTranslations[language][key] || key;
    };

    return (
        <LanguageHorticultureContext.Provider value={{ language, setLanguage: handleSetLanguage, t }}>
            {children}
        </LanguageHorticultureContext.Provider>
    );
};

export const useLanguageHorticulture = (): LanguageHorticultureContextType => {
    const context = useContext(LanguageHorticultureContext);
    if (context === undefined) {
        throw new Error('useLanguageHorticulture must be used within a LanguageHorticultureProvider');
    }
    return context;
};